    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String w = jTextField1.getText();
        confirm_donors conDonorObj = new confirm_donors();
        if (w == null || w.length() != 13) {
            JOptionPane.showMessageDialog(null, "Invalid Input..!");
        } else {
            boolean found = false;
            long cnic = Long.parseLong(w);
            found=conDonorObj.chkdonors(cnic);
            if (found == true) {
                System.out.print("yes cnic found...");
                ProjectWork p = new ProjectWork();
                p.CNIC = jTextField1.getText();
                System.out.println(p.CNIC);
                donate d = new donate();
                d.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "CNIC not found..!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

