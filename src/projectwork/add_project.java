    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if( pn.getText().isEmpty()|| t.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Fields cannot be empty .Input required in alll the fields .Again enter data");
        }
        else
        {
          
            String fn=pn.getText();
            String ar=t.getText();
           
            boolean fl=true;
            for (int hj=0;hj < ar.length();hj++)
            {
                if (ar.charAt(hj)<48  || ar.charAt(hj)>57)
                {
                    fl=false;
                    break;
                }
            }
            if (fl== false)
            {
             JOptionPane.showMessageDialog(null, "Total money required cannot have special characters and alphabets.Enter numeric data");
              t.setText(null);
               }
            else
            {
                ff=new ProjectClass();
                int id=ff.assignID();
                ff = new ProjectClass(id,fn,ar);
                ff.add();
       
                JOptionPane.showMessageDialog(null, "Project is added");
               Project_GUI m=new Project_GUI();
               m.setVisible(true);
               this.setVisible(false);
            }
          
        }
    }//GEN-LAST:event_jButton1ActionPerformed

