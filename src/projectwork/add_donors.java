    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        manageDonors obj = new manageDonors();
        String w = dname.getText().toString();
        String temp1 = dcnic.getText();
        String temp2 = dcno.getText();
        String z = dmail.getText();
        boolean result = false;

        if (w == null || temp1.length() != 13 || temp2.length() != 10 || z.indexOf('@') == -1) {
            if (w == null) {
                infomessage("Incorrect Name..!", "Alert Wrong Input !");

            } else if (temp1.length() != 13) {
                infomessage("Incorrect CNIC..!", "Alert Wrong Input !");

            } else if (temp2.length() != 10) {
                infomessage("Incorrect  Contact Number..!", "Alert Wrong Input !");

            } else if (z.indexOf('@') == -1) {
                infomessage("Incorrect Email..!", "Alert Wrong Input !");

            }
        } else {
            long x = Long.parseLong(temp1);
            long y = Long.parseLong(temp2);
            result = obj.set_data(w, x, y, z);
            if (result == true) {
                infomessage("Done..! Successfully Added..", "Alert !");
                 manage_donors ol=new manage_donors();
        ol.setVisible(true);
        this.setVisible(false);
            }
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

